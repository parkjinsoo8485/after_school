function createNavigator() {
  const container = document.createElement('div');
  container.id = 'stitch-navigator';
  container.style.cssText = 'position: fixed; bottom: 20px; right: 20px; z-index: 9999; font-family: sans-serif;';
  
  const button = document.createElement('button');
  button.innerText = 'Explore Screens';
  button.style.cssText = 'background: #137fec; color: white; border: none; padding: 10px 20px; border-radius: 50px; cursor: pointer; box-shadow: 0 4px 15px rgba(0,0,0,0.2); font-weight: bold;';
  
  const list = document.createElement('div');
  list.style.cssText = 'display: none; position: absolute; bottom: 50px; right: 0; background: white; border: 1px solid #ddd; border-radius: 10px; width: 300px; max-height: 400px; overflow-y: auto; box-shadow: 0 10px 30px rgba(0,0,0,0.1); padding: 10px;';
  
  screens.forEach(s => {
    const item = document.createElement('a');
    item.href = s.path;
    item.innerText = s.title;
    item.target = '_blank';
    item.style.cssText = 'display: block; padding: 8px 12px; color: #333; text-decoration: none; border-bottom: 1px solid #eee; font-size: 13px;';
    item.onmouseover = () => item.style.background = '#f5f5f5';
    item.onmouseout = () => item.style.background = 'transparent';
    list.appendChild(item);
  });
  
  button.onclick = (e) => {
    e.stopPropagation();
    list.style.display = list.style.display === 'none' ? 'block' : 'none';
  };
  
  document.addEventListener('click', () => list.style.display = 'none');
  list.onclick = (e) => e.stopPropagation();
  
  container.appendChild(list);
  container.appendChild(button);
  document.body.appendChild(container);
}

window.addEventListener('DOMContentLoaded', createNavigator);
